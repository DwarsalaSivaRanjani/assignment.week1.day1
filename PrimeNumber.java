package assignment.week1.day1;

public class PrimeNumber {

	public static void main(String[] args) {
		int number = 13;
		for (int i=2; i<number; i++)
		{
if (number%i==0)
{
System.out.println("13 is a prime number");
		}
		
		else
			System.out.println("13 is not a prime number");
	}

}
}



